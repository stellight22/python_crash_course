#4-10 slices

liszt = []
for x in range(1,33,6):
    liszt.append(x)
print(liszt)
print("The first three items in the list are ")
print(liszt[0:3])

print("Three itmes from the middle of the list are: ")
print(liszt[2:5])
# this starts are index 2 and ends at the 5th item

print("The last three items in the list are:")
print(liszt[-3:])

print("The fourth item from the end of the list is:")
print(liszt[-4])
#when counting backwards you start at -1


